import turtle
win = turtle.Screen()
t = turtle.Turtle()
t.pensize(5)
t.color("orange")
t.shape("turtle")

t.backward(100)
t.right(90)
t.forward(100)
t.left(90)
t.forward(100)
t.backward(100)
t.right(90)
t.forward(100)
t.left(90)
t.forward(100)

t.penup()
t.forward(50)
t.pendown()

t.left(90)
t.forward(200)
t.right(90)
t.forward(100)
t.right(90)
t.forward(100)
t.right(90)
t.forward(100)
t.backward(50)
t.left(130)
t.forward(100)

win.mainloop()
